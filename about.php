        function openProfile(evt, memberName) {
          var i, personContent, personLink;
          personLink = document.getElementsByClassName("personCard");
          personContent = document.getElementsByClassName("personContent");
          for (i = 0; i < personLink.length; i++) {
            personLink[i].className = personLink[i].className.replace(" active", "");
          }
          if(document.getElementById(memberName).style.display == "block")
          {
                evt.currentTarget.className.replace(" active", "");
              document.getElementById(memberName).style.display = "none";
          }else{
            for (i = 0; i < personContent.length; i++) {
                personContent[i].style.display = "none";
                document.getElementById(memberName).style.display = "block";
              }
                        evt.currentTarget.className += " active";
          }
        }
